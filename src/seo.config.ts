/** 메타타이틀 끝에 붙는 브랜드 꼬리표. 없애려면 빈 문자열('')로 두세요. */
export const BRAND_SUFFIX = ``;

/** 문자열 뒤에 브랜드 꼬리표를 붙여주는 도우미 (동적 페이지에서도 사용) */
export const withBrand = (core: string): string => `${core}${BRAND_SUFFIX}`;

/* ────────────────────────────────────────────────────────────────────────────
 *  [ 내가 직접 편집하는 SEO 항목 ]   ← 아래 필드로 이 파일에서 관리
 *    title          : 메타타이틀 (<title>)
 *    description    : 메타 디스크립션
 *    ogTitle        : (선택) SNS 공유용 제목. 비우면 title 을 그대로 사용
 *    ogDescription  : (선택) SNS 공유용 설명. 비우면 description 을 그대로 사용
 *    ogImage        : (선택) SNS 공유 썸네일 경로. 비우면 기본 이미지(og-default) 사용
 *
 *  [ 자동으로 붙는 항목 ]   ← 건드릴 필요 없음 (BaseLayout 이 자동 생성)
 *    canonical, og:url, og:type, og:site_name, og:locale,
 *    twitter:card, robots, favicon, sitemap, theme-color
 * ────────────────────────────────────────────────────────────────────────── */
type Meta = {
  /** 메타타이틀 (화면 H1과 무관. 여기서 자유롭게 짧게/키워드 중심으로) */
  title: string;
  /** 메타 디스크립션 (약 150자 내, 앞부분에 핵심/키워드) */
  description: string;
  /** (선택) SNS 공유용 제목. 비우면 title 사용 */
  ogTitle?: string;
  /** (선택) SNS 공유용 설명. 비우면 description 사용 */
  ogDescription?: string;
  /** (선택) 이 페이지 전용 OG 공유 이미지 경로. 비우면 기본 이미지 사용 */
  ogImage?: string;
};

